def _clean_lines(items, max_items=4):
    cleaned = [str(item).strip() for item in items if str(item or "").strip()]
    return cleaned[:max_items]


def _derive_context(lesson_request, teaching_design):
    topic = lesson_request.get("topic") or lesson_request.get("course_title") or "Senior English Lesson"
    topic_words = [word.strip(".,!?") for word in topic.split() if word.strip(".,!?")]
    keyword = topic_words[-1] if topic_words else "the topic"
    lesson_key = normalize_lesson_type(lesson_request.get("lesson_type"))

    lesson_labels = {
        "reading": "Reading and Thinking",
        "grammar": "Grammar Workshop",
        "writing": "Writing Workshop",
        "listening_speaking": "Listening and Speaking",
        "revision": "Revision Lesson",
    }
    output_labels = {
        "reading": "a short reading response",
        "grammar": "a short grammar-based response",
        "writing": "a guided writing draft",
        "listening_speaking": "a short spoken response",
        "revision": "a mixed review task",
    }
    vocabulary_sets = {
        "reading": ["main idea", "supporting detail", "text evidence"],
        "grammar": ["sentence pattern", "language form", "common mistake"],
        "writing": ["topic sentence", "supporting detail", "linking word"],
        "listening_speaking": ["key information", "useful expression", "spoken response"],
        "revision": ["key word", "grammar clue", "revision strategy"],
    }

    return {
        "topic": topic,
        "unit": lesson_request.get("unit") or "Unit 1",
        "grade": lesson_request.get("grade") or "高一",
        "textbook": lesson_request.get("textbook") or "人教版",
        "lesson_label": lesson_labels.get(lesson_key, "Senior English Lesson"),
        "lesson_key": lesson_key,
        "keyword": keyword,
        "output_label": output_labels.get(lesson_key, "a classroom task"),
        "vocabulary": vocabulary_sets.get(lesson_key, vocabulary_sets["reading"]),
        "objectives": teaching_design.get("teaching_objectives") or [],
        "key_points": teaching_design.get("key_points") or [],
        "difficult_points": teaching_design.get("difficult_points") or [],
        "teaching_methods": teaching_design.get("teaching_methods") or [],
        "extra_requirements": lesson_request.get("extra_requirements") or "",
    }


def _base_slide(lesson_request, slide_outline, teaching_design):
    context = _derive_context(lesson_request, teaching_design)
    return {
        "slide_index": slide_outline.get("slide_index"),
        "slide_type": slide_outline.get("slide_type"),
        "title": slide_outline.get("title") or "Untitled Slide",
        "visible_content": [],
        "teacher_notes": "",
        "teaching_purpose": slide_outline.get("teaching_purpose") or "",
        "estimated_time": slide_outline.get("estimated_time") or "3 minutes",
        "interaction_type": "Teacher-student interaction",
        "_context": context,
    }


