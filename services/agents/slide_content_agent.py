def _apply_extra_requirement(slide):
    context = slide["_context"]
    extra = context.get("extra_requirements")
    if not extra:
        return slide
    if slide["slide_type"] in {"group_discussion", "communicative_practice", "guided_writing", "pair_work"}:
        slide["visible_content"] = _clean_lines(
            list(slide.get("visible_content", [])) + [f"Teacher focus: {extra}"]
        )
    return slide


def _generate_rule_slide_from_outline(
    lesson_request,
    teaching_design,
    slide_outline,
    regenerate=False,
):
    slide = _base_slide(lesson_request, slide_outline, teaching_design)
    builder = SLIDE_BUILDERS.get(slide["slide_type"], _summary_slide)
    slide = builder(slide)
    slide = _apply_extra_requirement(slide)
    if regenerate:
        slide["teacher_notes"] = f"{slide['teacher_notes']} Keep the activity fresh by asking for a different student example this time."
    slide.pop("_context", None)
    return slide


def generate_rule_slide_content(lesson_request, teaching_design, ppt_outline):
    """Return the local rule-based slide collection without any model call."""

    slides = [
        _generate_rule_slide_from_outline(lesson_request, teaching_design, slide_outline)
        for slide_outline in ppt_outline
    ]
    return check_schema(slides)


def _knowledge_context_block(knowledge_context):
    if not knowledge_context:
        return ""
    return "\n\n" + trim_knowledge_context_for_prompt(format_knowledge_context_for_prompt(knowledge_context), max_chars=4000)


def generate_slide_content(lesson_request, teaching_design, ppt_outline, knowledge_context=None):
    """Generate classroom-ready slide content for the full outline."""

    fallback = generate_rule_slide_content(lesson_request, teaching_design, ppt_outline)

    generated = []
    chunk_size = 4
    for start in range(0, len(ppt_outline), chunk_size):
        outline_chunk = ppt_outline[start:start + chunk_size]
        fallback_chunk = fallback[start:start + chunk_size]
        stage = f"slide_content_batch_{start // chunk_size + 1}"
        system_prompt, user_prompt = _slide_content_prompts(
            lesson_request,
            teaching_design,
            outline_chunk,
            fallback_chunk,
            knowledge_context=knowledge_context,
        )
        payload = call_agent_json(
            "slide_content_agent",
            {
                "system_prompt": system_prompt,
                "user_prompt": user_prompt,
                "task_id": lesson_request.get("task_id"),
                "stage_note": stage,
            },
            fallback_fn=lambda chunk=fallback_chunk: chunk,
        )
        generated.extend(_normalize_slide_collection(payload, fallback_chunk))
    return check_schema(generated)


def generate_single_slide_content(
    lesson_request,
    teaching_design,
    slide_outline,
    regenerate=False,
    knowledge_context=None,
):
    """Generate or regenerate a single slide from outline data."""

    fallback = _generate_rule_slide_from_outline(
        lesson_request,
        teaching_design,
        slide_outline,
        regenerate=regenerate,
    )
    system_prompt, user_prompt = _single_slide_content_prompts(
        lesson_request,
        teaching_design,
        slide_outline,
        fallback,
        regenerate=regenerate,
        knowledge_context=knowledge_context,
    )
    payload = call_agent_json(
        "slide_content_agent",
        {
            "system_prompt": system_prompt,
            "user_prompt": user_prompt,
            "task_id": lesson_request.get("task_id"),
            "stage_note": "slide_content_single",
        },
        fallback_fn=lambda: fallback,
    )
    return _normalize_single_slide(payload, fallback)


def _normalize_visible_content(value, fallback):
    if isinstance(value, list):
        cleaned = _clean_lines(value, max_items=4)
        if cleaned:
            return cleaned
    return _clean_lines(fallback, max_items=4)


def _normalize_slide_index(value, fallback):
    try:
        return int(value)
    except (TypeError, ValueError):
        return int(fallback)


def _normalize_slide(candidate, fallback):
    candidate = candidate if isinstance(candidate, dict) else {}
    normalized = {
        "slide_index": _normalize_slide_index(
            candidate.get("slide_index"),
            fallback["slide_index"],
        ),
        "slide_type": str(candidate.get("slide_type") or fallback["slide_type"]).strip(),
        "title": str(candidate.get("title") or fallback["title"]).strip(),
        "visible_content": _normalize_visible_content(
            candidate.get("visible_content"),
            fallback.get("visible_content", []),
        ),
        "teacher_notes": str(candidate.get("teacher_notes") or fallback["teacher_notes"]).strip(),
        "teaching_purpose": str(
            candidate.get("teaching_purpose") or fallback["teaching_purpose"]
        ).strip(),
        "estimated_time": str(candidate.get("estimated_time") or fallback["estimated_time"]).strip(),
        "interaction_type": str(
            candidate.get("interaction_type") or fallback["interaction_type"]
        ).strip(),
    }
    return check_schema(normalized)


def _normalize_slide_collection(payload, fallback):
    candidate_slides = payload.get("slides") if isinstance(payload, dict) else payload
    if not isinstance(candidate_slides, list):
        return fallback

    slides_by_index = {}
    for slide in candidate_slides:
        if not isinstance(slide, dict):
            continue
        try:
            slide_index = int(slide.get("slide_index"))
        except (TypeError, ValueError):
            continue
        slides_by_index[slide_index] = slide
    normalized = []
    for index, fallback_slide in enumerate(fallback, start=1):
        candidate = slides_by_index.get(fallback_slide["slide_index"])
        if candidate is None and index - 1 < len(candidate_slides):
            candidate = candidate_slides[index - 1]
        normalized.append(_normalize_slide(candidate, fallback_slide))
    return check_schema(normalized)


def _normalize_single_slide(payload, fallback):
    if isinstance(payload, dict) and isinstance(payload.get("slide"), dict):
        payload = payload["slide"]
